<?php

namespace Nicolas\ProjectManager\Entity;

class UserAccount
{
    private int $id;
    private string $email;
    private string $password;
    private string $firstName;
    private string $lastName;

    public function getId(): int
    {
        return $this->id;
    }

    public function getEmail(): string
    {
        return $this->email;
    }

    public function setEmail(string $email): UserAccount
    {
        $this->email = $email;
        return $this;
    }

    public function getPassword(): string
    {
        return $this->password;
    }

    public function setPassword(string $password): UserAccount
    {
        $this->password = $password;
        return $this;
    }

    public function getFirstName(): string
    {
        return $this->firstName;
    }

    public function setFirstName(string $firstName): UserAccount
    {
        $this->firstName = $firstName;
        return $this;
    }

    public function getLastName(): string
    {
        return $this->lastName;
    }

    public function setLastName(string $lastName): UserAccount
    {
        $this->lastName = $lastName;
        return $this;
    }
}
